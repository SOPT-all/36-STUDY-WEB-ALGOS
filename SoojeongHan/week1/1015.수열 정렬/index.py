# 문제 이해를 못 함. 

# 배열 A
# P[i]로 B[P[i]] = A[i]가 되도록
# B가 오름차순(비내림차순)이 되도록
# P[N]은 0부터 N-1까지 숫자를 한 번씩

# 단계

# A 배열의 값과 인덱스를 같이 저장
# 예시: A = [2, 3, 1] → [(2, 0), (3, 1), (1, 2)]

# 작은 값이 앞으로 오도록 값을 기준으로 정렬
# 정렬 결과: [(1, 2), (2, 0), (3, 1)]

# 정렬한 순서대로 새로운 인덱스를 부여
# 값이 가장 작은 A[2]는 B[0]에 와야 하니까 P[2] = 0
# 다음으로 작은 A[0]는 B[1] → P[0] = 1
# 마지막 A[1]는 B[2] → P[1] = 2

# 결과 P를 출력하면 정답!
# → P = [1, 2, 0]

n = int(input())
A = list(map(int, input().split()))

# A의 값과 원래 인덱스를 저장
temp = []
for i in range(n):
    temp.append([A[i], i])  # [값, 원래 위치]

# 값 기준으로 오름차순 정렬
temp.sort()

# P 배열을 만들기 위한 리스트 초기화
P = [0] * n

# 정렬된 순서대로 순위 번호를 원래 위치에 기록
for i in range(n):
    value, original_index = temp[i]
    P[original_index] = i

# 출력
print(*P)
